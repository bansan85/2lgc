/*!

\namespace norme
\brief Classes non \c %POCO ayant un lien avec les normes de calculs.

Ce paquet contient les classes ayant un lien avec les calculs de modélisation, hors calculs liés aux normes.

\namespace POCO::calc
\brief Rubrique calcul.

Ce paquet contient les classes ayant un lien avec les calculs de modélisation, hors calculs liés aux normes.

\namespace POCO::nombre
\brief Classes implémentant l'interface INb.

Ce paquet contient les classes permettant de stocker des nombres sous divers formes.

\namespace POCO::sol
\brief Rubrique sollicitation.

Ce paquet contient les classes ayant un lien avec les charges appliquées sur la structure, hors données des normes.

\namespace POCO::sol::action
\brief Rubrique action des sollicitations.

Ce paquet contient les classes stockant les actions appliquées sur la structure.

\namespace POCO::sol::charge
\brief Classes implémentant l'interface ICharge.

Ce paquet contient les classes permettant de stocker des charges sous divers formes (ponctuelles, réparties, …).

\namespace POCO::str
\brief Rubrique structure.

Ce paquet contient les classes permettant la modélisation de la structure.

\namespace POCO::str::deplacementType
\brief Classes implémentant l'interface IDeplacementType.

Ce paquet contient les classes permettant de modéliser des relations force / déplacement.

\namespace POCO::str::materiau
\brief Classes implémentant l'interface IMateriau.

Ce paquet contient les classes permettant de modéliser des matériaux. Chaque classe est préfixée par l'abbréviation de la norme à laquelle elle s'applique.

\namespace POCO::str::noeud
\brief Classes implémentant l'interface INoeud.

Ce paquet contient les classes permettant de modéliser des nœuds. Il existe deux types de nœuds, les libres ayant des coordonnées en 3D et des nœuds connectés à une barre avec comme donnée la distance entre la position du début de la barre et la sienne.

\namespace POCO::str::section
\brief Classes implémentant l'interface ISection.

Ce paquet contient les classes permettant de modéliser des sections applicable sur des barres.

*/
